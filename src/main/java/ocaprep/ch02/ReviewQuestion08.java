package ocaprep.ch02;

public class ReviewQuestion08 {
	/*
	 * what is the output of the following code snippet?
	 */
	public static void main(String[] args) {
		/*
		 * each subsequent variable can be declared with the same data type on the same line separated with a comma
		 */
		boolean x = true, z = true;
		int y = 20;
		System.out.println(x + ", " + y + ", " + z); // x: true, y: 20, z: true
	}
}
